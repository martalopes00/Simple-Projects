# Pedir ao utilizador para inserir um número
n1 = int(input('Escreve um número: '))

# Pedir ao utilizador para inserir outro número
n2 = int(input('Escreve outro número: '))

# Somar os dois valores
soma = n1 + n2

# Mostrar a soma dos dois valores
print('A soma de {} e {} é {}'.format(n1, n2, soma))


