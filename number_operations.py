# Ask the user to enter a number
n = int(input('Enter a number: '))

# Show the double of the number
print('The double of {} is {}'.format(n, n*2))

# Show the triple of the number
print('The triple of {} is {}'.format(n, n*3))

# Show the square root of the number
print('The square root of {} is {}'.format(n, n**(1/2)))
